# Example of using colors in the terminal
print('\033[31mThis text is red\033[m')
print('\033[32mThis text is green\033[m')
print('\033[33mThis text is yellow\033[m')
print('\033[34mThis text is blue\033[m')
print('\033[35mThis text is magenta\033[m')
print('\033[36mThis text is cyan\033[m')

# Background colors
print('\033[41mThis text has a red background\033[m')
print('\033[42mThis text has a green background\033[m')

# Style, text color, and background combined
print('\033[1;37;44mBold white text on blue background\033[m')
